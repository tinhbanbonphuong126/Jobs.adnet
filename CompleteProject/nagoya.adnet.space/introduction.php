        <section class="section-article wrap-boat">
            <div class="big-title"><a name="4">施設の特徴</a></div>
            <article>
                <div class="small-title">1.焼却灰等の全量資源化（埋立処分量ゼロ）</div>
                <div class="article-content">
                    ごみを直接溶融して、溶融スラグとメタルを生成し、資源として活用します。また、溶融飛灰と呼ばれる細かい灰についても、外部の資源化企業で、金属成分を資源化します。
                </div>
            </article>
            <article>
                <div class="small-title">2.施設の強靭化及び一時避難機能としての活用</div>
                <div class="article-content">
                    本施設は大規模地震への耐震性を有しており、災害時には施設利用者の安全確保を最優先します。また、同時に地域の方々の一時避難場所として機能する計画とします。
                    災害時において電力会社の送電が停電しても、ごみ処理の再開や一時避難場所への電気が供給できるガス発電機を設置します。災害発生時の上水断水に備えて、1日 2,000㍑の飲料水を7日分確保できるようにします。
                </div>
            </article>
            <article>
                <div class="small-title">3.見学者等環境学習対応設備</div>
                <div class="article-content">
                    学習希望者の方が来場された際には、ごみの減量や分別意識に対する啓発等を行うための設備や、施設関連の資料展示する環境学習施設を設け、また、地域住民の方の憩いの場として活用できるよう、休日等の利用も考慮します。
                </div>
            </article>
            <article>
                <div class="small-title">4.余熱利用設備</div>
                <div class="article-content">
                    地元への還元施設として、工場からの余熱を利用した温水プールの整備について、現在、北名古屋市、豊山町、名古屋市との間で協議を進めています。
                </div>
            </article>
            <article>
                <div class="small-title">5.雨水排水対策</div>
                <div class="article-content">
                    工場敷地内には、5,000ｍ3の雨水貯留槽を設置し、工場敷地内及び工場周辺に降った雨を一旦貯留してから、徐々に排出することで、工場から下流の排水路に対する影響を軽減します。
                </div>
            </article>
